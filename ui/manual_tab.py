"""
Вкладка «Инструкция» — WorkdayMonitor v2.0.
"""
import customtkinter as ctk
from . import theme as T

MANUAL_TEXT = """
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
   Инструкция по работе с приложением
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

КАК НАЧАТЬ РАБОЧИЙ ДЕНЬ
────────────────────────────────────────────────
1. Приложение запускается автоматически при включении ПК
2. Нажмите кнопку «Начать день»
3. Приложение начнёт записывать вашу активность

КАК ЗАПИСЫВАТЬ ЗАДАЧИ
────────────────────────────────────────────────
1. Введите название задачи в поле «Название операции»
2. Нажмите «Старт»
3. Когда задача завершена — нажмите «Стоп»
4. Если нужно прерваться — нажмите «Пауза»

КАК ЗАВЕРШИТЬ РАБОЧИЙ ДЕНЬ
────────────────────────────────────────────────
1. Нажмите кнопку «Завершить день»
2. Приложение автоматически сформирует отчёт
3. Отчёт отправится руководителю — вам ничего делать не нужно

ЧАСТО ЗАДАВАЕМЫЕ ВОПРОСЫ
────────────────────────────────────────────────
В: Нужно ли каждый день запускать приложение вручную?
О: Нет. Приложение запускается само при включении компьютера.

В: Что делать, если я ушёл на обед и не нажал «Пауза»?
О: Ничего страшного. Приложение автоматически определяет паузу,
   если компьютер неактивен более 5 минут.

В: Что будет, если я забуду нажать «Завершить день»?
О: Отчёт сформируется автоматически при выключении ПК.

В: Приложение видит содержимое моих файлов и переписку?
О: Нет. Только названия программ и время работы в них.

В: Куда уходят мои данные?
О: Отчёты отправляются только руководителю компании.
"""

class ManualTab(ctk.CTkFrame):
    def __init__(self, parent, **kwargs):
        super().__init__(parent, fg_color=T.BG_DARK, **kwargs)
        self._build()

    def _build(self):
        self.grid_rowconfigure(0, weight=1)
        self.grid_columnconfigure(0, weight=1)
        text = ctk.CTkTextbox(self, wrap="word",
                               font=ctk.CTkFont(T.FONT_SANS, 12),
                               fg_color=T.BG_CARD,
                               text_color=T.TEXT_SECONDARY,
                               corner_radius=8)
        text.grid(row=0, column=0, sticky="nsew", padx=16, pady=16)
        text.insert("1.0", MANUAL_TEXT)
        text.configure(state="disabled")
